import java.util.*;

public class primitiveDT {
    public static void main(String args[]) {
        int x = 5;
        int y = 7;
        System.out.println("x is : " + x);
        System.out.println("y is : " + y);
        byte B1 = (byte) 130;
        System.out.println("B1 is : " + B1);
        byte b = 10;
        System.out.println("B is : " + b);
        int i = b;
        System.out.println("I is : " + i);

    }

}
